public class TipeDataPrimitive {
    public static void main(String[] args) {
        // Deklarasi dan assignment tipe data integer
        int jumlahProduk = 50;
        System.out.println("Jumlah Produk: " + jumlahProduk);

        // Deklarasi dan assignment tipe data floating point
        double hargaProduk = 99.99;
        System.out.println("Harga Produk: " + hargaProduk);

        // Deklarasi dan assignment tipe data boolean
        boolean stokTersedia = true;
        System.out.println("Stok Tersedia: " + stokTersedia);

        // Deklarasi dan assignment tipe data karakter
        char kategoriProduk = 'A';
        System.out.println("Kategori Produk: " + kategoriProduk);
    }
}