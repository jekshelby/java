public class AssignmentDeklarasi {

    public static void main(String[] args) {
        int a = 10; // bentuk dari assignment
        // mendeklarasikan variabel a sebagai tipe int, tapi juga langsung memberikan nilai awal padanya.
        System.out.println("nilai a " + a);
        
        a = 20;
        System.out.println("nilai a yang baru " + a);

        int b; // bentuk dari deklarasi
        // memberitahu kompiler bahwa kita memiliki variabel yang bernama b, yang akan digunakan untuk menyimpan nilai-nilai yang bertipe int
        b = 8;
        System.out.println(b);
    }
}